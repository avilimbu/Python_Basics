fruits = ["Apple", "mango", "banana", "cherry"]
print(fruits)

#lists methods

#append
fruits.append("Graps")
print(fruits)

#sort 
l1 =[1,2,6,3,9,7]
l1.sort()
print(l1)

#reverse
l1.reverse()
print(l1)

#insert 
l1.insert(3,33333)
print(l1)

#pop
l1.pop() #removing the last item
print(l1)

l1.pop(3)
print(l1)

#remove()
l1.remove(8) #removes 6 from the data
print(l1)